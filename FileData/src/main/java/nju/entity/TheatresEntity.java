package nju.entity;

import javax.persistence.*;

/**
 * Created by cuihao on 2017-06-10.
 *
 */
@Entity
@Table(name = "theatres")
public class TheatresEntity {
    private int id;
    private String name;
    private String address;
    private String phone;
    private PlatformsEntity platformsEntity;

    @ManyToOne
    @JoinColumn(name = "platform_id", referencedColumnName = "id")
    public PlatformsEntity getPlatformsEntity() {
        return platformsEntity;
    }

    public void setPlatformsEntity(PlatformsEntity platformsEntity) {
        this.platformsEntity = platformsEntity;
    }

    @Id
    @Column(name = "id")
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Basic
    @Column(name = "name")
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Basic
    @Column(name = "address")
    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    @Basic
    @Column(name = "phone")
    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        TheatresEntity that = (TheatresEntity) o;

        if (id != that.id) return false;
        if (name != null ? !name.equals(that.name) : that.name != null) return false;
        if (address != null ? !address.equals(that.address) : that.address != null) return false;
        if (phone != null ? !phone.equals(that.phone) : that.phone != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = id;
        result = 31 * result + (name != null ? name.hashCode() : 0);
        result = 31 * result + (address != null ? address.hashCode() : 0);
        result = 31 * result + (phone != null ? phone.hashCode() : 0);
        return result;
    }
}
